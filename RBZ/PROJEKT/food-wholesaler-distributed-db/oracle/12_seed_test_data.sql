    SPRZEDAZ.PKG_SPRZEDAZ.sp_dodaj_pozycje(v_zam2, 3, 5);    -- 5 x olej
    SPRZEDAZ.PKG_SPRZEDAZ.sp_dodaj_pozycje(v_zam2, 5, 8);    -- 8 x pierogi

    -- klient 3
