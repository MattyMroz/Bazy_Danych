    SPRZEDAZ.PKG_SPRZEDAZ.sp_dodaj_pozycje(v_zam3, 4, 15);   -- 15 x pieprz

    DBMS_OUTPUT.PUT_LINE('Utworzono zamowienia: ' || v_zam1 || ', ' || v_zam2 || ', ' || v_zam3);
